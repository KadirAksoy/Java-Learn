package org.kadiraksoy;

public class _01_Variable {
    public static void main(String[] args) {


        // Primitive type :
        // "Primitive type" (ilkel veri türü), programlama dillerinde temel veri türlerini ifade eden
        // ve dilin kendisi tarafından desteklenen basit veri türleridir.

        String str = "kadir";
        char ch = 'c';

        boolean isTrue = true;
        boolean isFalse = false;

        int number = 1;
        double doubleNumber = 2.025;
        float floatNumber = 4.15f;

        long longNumber = 1234L;
        short shortNumber = 32000;

        byte byteNumber = 127;

        // Wrapper Class :her biri ilkel veri türlerine (boolean, byte, short, int, long, float, double ve char)
        // karşılık gelen sınıflar vardır. Bu sınıflar "Wrapper class" olarak adlandırılır.
        // Her bir ilkel veri türü için bir Wrapper class bulunur
        // ve ilkel veri türleri ile ilgili işlemleri gerçekleştirmek için kullanılırlar.
        // Wrapper class'lar, ilkel veri türlerini nesne olarak kullanma imkanı sağlar.
        // İlkel veri türlerini sınıf olarak sarmalarlar
        // ve bu sayede ilkel veri türlerinin metodlarını ve işlevlerini kullanma olanağı sunarlar.
        // Ayrıca, wrapper class'lar, ilkel veri türlerini nesne olarak kullanmanın yanı sıra, birçok faydalı yöntem
        // ve işlevler de sağlar.

        Boolean myBoolean = true;

        Byte myByte = 10;

        Short myShort = 100;

        Integer myInteger = 1000;

        Long myLong = 100000L;

        Float myFloat = 3.14f;

        Double myDouble = 3.14159;

        Character myCharacter = 'A';







    }
}
